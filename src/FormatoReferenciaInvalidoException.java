public class FormatoReferenciaInvalidoException extends Exception {

    public FormatoReferenciaInvalidoException() {
        super();
    }

    public FormatoReferenciaInvalidoException(String message) {
        super(message);
    }

    public FormatoReferenciaInvalidoException(String message, Throwable cause) {
        super(message, cause);
    }

    public FormatoReferenciaInvalidoException(Throwable cause) {
        super(cause);
    }
}
